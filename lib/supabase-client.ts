"use server"

import { createClient } from "@supabase/supabase-js"
import type { Database } from "@/types/supabase"

let supabaseClient: ReturnType<typeof createClient<Database>> | null = null

export async function getSupabaseClient() {
  if (supabaseClient) return supabaseClient

  const url = process.env.NEXT_PUBLIC_SUPABASE_URL
  const key = process.env.SUPABASE_SERVICE_KEY

  if (!url || !key) {
    console.error("‚ùå Supabase env vars missing:", {
      hasUrl: !!url,
      hasKey: !!key,
    })
    return null
  }

  try {
    console.log("üîÑ Inicializando cliente de Supabase...")
    supabaseClient = createClient<Database>(url, key)

    // Verificar conexi√≥n con una consulta simple
    const { data, error } = await supabaseClient.from("sessions").select("count").limit(1).single()

    if (error) {
      if (error.code === "PGRST116") {
        console.warn("‚ö†Ô∏è La tabla 'sessions' no existe. Esto puede ser normal si es la primera ejecuci√≥n.")
      } else {
        console.error("‚ùå Error al verificar conexi√≥n con Supabase:", error)
      }
    } else {
      console.log("‚úÖ Conexi√≥n a Supabase verificada correctamente")
    }

    return supabaseClient
  } catch (error) {
    console.error("‚ùå Error al inicializar cliente de Supabase:", error)
    return null
  }
}
